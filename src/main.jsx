// الملف ده هو "زرار التشغيل الرئيسي" أو "الفيشة" بتاعة المشروع كله.
// من غير الملف ده، كل الصفحات والأكواد اللي عملناها ملهاش لازمة ومش هتظهر على الشاشة.
// وظيفته إنه ياخد كل الشغل بتاعنا ويزرعه جوه صفحة المتصفح عشان اليوزر يشوفه.

// بنجيب أداة اسمها StrictMode من مكتبة React. 
// الأداة دي عاملة زي "مراقب الجودة" أو "المفتش الدقيق"، وظيفتها تراقب الكود وإحنا شغالين وتطلعلنا أي أخطاء مخفية أو حاجات ممكن تبوظ الأداء.
import { StrictMode } from 'react'

// بنجيب أداة اسمها createRoot. 
// دي الأداة اللي بتاخد كود الـ React بتاعنا (اللي المتصفح العادي مش بيفهمه لوحده) وتترجمه وترسمه جوه صفحة الـ HTML العادية.
import { createRoot } from 'react-dom/client'

// هنا بنستورد ملف التصميمات الأساسية (CSS) اللي فيه التظبيطات العامة للموقع كله (زي الخطوط والألوان الأساسية).
import './index.css'

// بنستورد ملف App.jsx اللي إحنا لسه شارحينه (عسكري المرور اللي جواه خريطة الموقع والصفحات كلها).
import App from './App.jsx'

// السطر ده هو "لحظة التشغيل الفعلي للموقع".
// 1. (document.getElementById('root')) بنقول للمتصفح: روح دوّر جوه ملف الـ HTML الأساسي على صندوق (مربع) فاضي اسمه 'root' (الجذر أو الأساس).
// 2. (createRoot...render) بنقول للأداة بتاعتنا: خدي الكود بتاعنا كله وارسميه جوه الصندوق الفاضي ده.
createRoot(document.getElementById('root')).render(
  // إحنا هنا بنقوله ارسم الموقع بتاعنا <App />
  // بس حطيناه جوه الغلاف بتاع <StrictMode> عشان "مراقب الجودة" يفضل باصص على الموقع كله وهو شغال ويطمن إن مفيش أي كود مكتوب بطريقة غلط أو قديمة.
  <StrictMode>
    <App />
  </StrictMode>,
)